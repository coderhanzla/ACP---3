x = input("Enter the first number: ")
y = input("Enter the second number: ")
z = input("Enter the third number: ")

print("Before swapping:")
print("First number:", x)
print("Second number:", y)
print("Third number:", z)

temp = x
x = y
y = z
z = temp

print("After swapping:")
print("First number:", x)
print("Second number:", y)
print("Third number:", z)
